const { Sequelize } = require('sequelize');
const config = require('../config/database');

const sequelize = new Sequelize(
  config.database,
  config.username,
  config.password,
  {
    host: config.host,
    dialect: 'mysql',
    logging: false,
    pool: {
      max: 5,
      min: 0,
      acquire: 30000,
      idle: 10000
    }
  }
);

const models = {
  User: require('./user')(sequelize),
  Client: require('./client')(sequelize),
  Application: require('./application')(sequelize),
  Appointment: require('./appointment')(sequelize),
  Document: require('./document')(sequelize),
  FamilyMember: require('./familyMember')(sequelize)
};

// Set up associations
Object.keys(models).forEach(modelName => {
  if (models[modelName].associate) {
    models[modelName].associate(models);
  }
});

module.exports = {
  sequelize,
  ...models
};